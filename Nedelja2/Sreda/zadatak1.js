// За два броја (а и б) исписати њихов: 
// Збир,разлику,производ,количник,остатак при дељењу,степен (а^b)

let a = 25
let b = 2

let zbir = a + b
console.log('Zbir je', zbir)

let razlika = a - b
console.log('Razlika je', razlika)

let proizvod = a * b
console.log('Proizvod je', proizvod)

let kolicnik = a / b
console.log('Kolicnik je', kolicnik)

let ostatak = a % b
console.log('Ostatak pri deljenju je', ostatak)

let stepen = a ** b
console.log('Broj a stepenovan brojem b iznosi', stepen)